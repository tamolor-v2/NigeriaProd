SELECT PK,
       ACTIVE,
       TO_CHAR(CREATE_DATE, 'yyyymmddhh24miss'),
       DELETED,
       TO_CHAR(LAST_MODIFIED, 'yyyymmddhh24miss'),
       ACTIVATION_CODE,
       ACTIVITY,
       AFFECTED_RECORD,
       TO_CHAR(DATE_LOGGED, 'yyyymmddhh24miss'),
       ENROLLMENT_REF,
       FULL_NAME,
       KIT_TAG,
       MAC_ADDRESS,
       SYNCHED,
       USERNAME,
       TO_CHAR(ACTIVITY_END_TIME, 'yyyymmddhh24miss'),
       TO_CHAR(ACTIVITY_START_TIME, 'yyyymmddhh24miss'),
       DURATION,
       UNIQUE_ACTIVITY_CODE,
       REALTIME_DEVICE_ID,
       REF_DEVICE_ID,
       APP_VERSION,
       FAILURE_COUNT,
       FAILURE_REASON,
       NETWORK_STRENGTH,
       SYNC_FILE_NAME,
       SYNC_FILE_STATUS,
       SYNC_TRANSFER_MODE,
       NETWORK_TYPE
  FROM BIOCAPTURE.CLIENT_ACTIVITY_LOG
 WHERE TRUNC(CREATE_DATE) >= TO_DATE('${DATE}', 'yyyymmdd')
  AND TRUNC(CREATE_DATE) < TO_DATE('${DATE}', 'yyyymmdd')+1